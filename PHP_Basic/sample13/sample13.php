/**ファイルの読み込み
 * require：読み込みたいファイルがないとエラーになる
 * require_once：同じファイルを複数回読み込んでも１回しか読み込まない
 * include：読み込みたいファイルがなくてもエラーにならない
 * include_once：同じファイルを複数回読み込んでも１回しか読み込まない
 * ファイルのディレクトリが違う場合は、絶対パスで指定する
 * 
 * マジック定数
 * __FILE__：現在のファイルのパス
 * __LINE__：現在の行数
 * __DIR__：現在のディレクトリのパス
 * __FUNCTION__：現在の関数名
 * __CLASS__：現在のクラス名
 * __METHOD__：現在のメソッド名
 * __NAMESPACE__：現在の名前空間
 * 
 * namespace：名前空間を定義する
 * use：名前空間をインポートする
 * Laravelなどのフレームワークでよく使われる
 */

 

